    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int posicion = buscarProducto();
        
        if(posicion==-1){
            JOptionPane.showMessageDialog(this, "Producto No Encontrado","Busqueda"
                    ,JOptionPane.ERROR_MESSAGE);
        }else{
            datosProducto(posicion);
            this.btnRegistrar.setEnabled(true);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        int posicion = buscarProducto();
        
        clsProducto producto;
        producto = this.lstProductos.get(posicion);
        
        this.objTienda.agregarProducto(producto);
        JOptionPane.showMessageDialog(this, "Producto Registrado Con Éxito");
        this.txtCodigoBarras.setText("");
        this.btnRegistrar.setEnabled(false);

        this.btnBuscar.setEnabled(false);
    }//GEN-LAST:event_btnRegistrarActionPerformed

