    public int buscarProducto(){
        int posicion=-1;
        long codigo = Long.parseLong(this.txtCodigoBarras.getText());
        
        for(int i=0;i<this.lstProductos.size();i++){
            if(codigo == this.lstProductos.get(i).getCodigoBarras()){
                posicion=i;
                break;
            }
        }
        
        return posicion;
    }
    
     public void datosProducto(int posicion){
        SimpleDateFormat formatoFV = new SimpleDateFormat("dd/MM/YYY");
            String fechaVen = formatoFV.format(this.lstProductos.
                get(posicion).getFechaVen().getTime());

        SimpleDateFormat formatoFB = new SimpleDateFormat("dd/MM/YYY");
        String fechaFab = formatoFB.format(this.lstProductos.
               get(posicion).getFechaFab().getTime());

        Object matrizProd [][] = new Object[1][5];

        matrizProd[0][0]=this.lstProductos.get(posicion).getCodigoBarras();
        matrizProd[0][1]=this.lstProductos.get(posicion).getNombre();
        matrizProd[0][2]=fechaVen;
        matrizProd[0][3]=fechaFab;
        matrizProd[0][4]=this.lstProductos.get(posicion).getPrecio();

        tablaProductos.setModel(new javax.swing.table.DefaultTableModel(
            matrizProd, 
            new String [] {
                "Codigo", "Nombre", "Fecha Vencimiento", "Fecha Fabricación", "Precio"
            } 
        ));
    }
    
